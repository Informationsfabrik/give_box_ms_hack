<h1>Münster's Givebox-Network</h1>

<p>
    Wir eröffnen für Giveboxen in Münster eine digitale Community und schaffen einfachen digitalen 
    Zugang zu Infos, Events und Austausch mit Nutzern und Paten.
</p>

<p>
    <b>Innovationsgrad</b>: Wir erschaffen den digitalen Zwilling einer Givebox. Per QR-Code kommt 
    ihr zur Homepage der Box, könnt Bilder hochladen, Feedback geben und Euch mit anderen Nutzern 
    austauschen. Das gibt's bisher nur statisch und nicht interaktiv wie bei uns!
</p>

<p>
    <b>Umsetzbarkeit</b>: Die Paten bestehender Giveboxen können sich bei uns digital registrieren 
    und erhalten einen individuellen QR-Code nebst durch die Stadt angefertigte Schilder zu 
    Anbringung an der Box. In der Zukunft gibts eine kleine Boxerweiterung, die auch ohne Handy 
    einfache Meldung an die Paten der Box ermöglicht (z.B. bei Beschädigung oder schlechtem Zustand) 
    und durch Solar betrieben wird.
</p>

<p>
    <b>Wiederverwertung und Nachhaltigkeit</b>: Mit dem Givebox-Net fördern wir die Wiederverwertung 
    von Dingen direkt und motivieren zur Eröffnung eigener Giveboxen. Wir machen Nutzer zu Paten und 
    erhöhen die Chancen zur Wiederverwertung durch bessere Transparenz und Suchfunktionen.
</p>

<p>
    <b>Nutzen für den Standort Münster</b>: Die Betreuung von Giveboxen ist oftmals eine 
    Herausforderung und verwaiste oder beschädigte Giveboxen rücken das  ehrenamtliche Engagement 
    der Paten in ein schlechtes Licht. Wir helfen Missstände der Boxen zu verbessern und bieten 
    Unterstützung dieses tolle Angebot nicht nur aufrecht zu erhalten, sondern zu erweitern.
</p>